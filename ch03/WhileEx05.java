package ch03;

public class WhileEx05 {
	public static void main(String[] args) {
		// 100까지 홀수 출력
		int a=1;
		while(a<101) {
			System.out.println(a);
			a+=2;
		}
		
		System.out.println("----------");
		
		// 100까지 짝수 출력
		int b=2;
		while(b<101) {
			System.out.println(b);
			b+=2;
		}
		
		
	}
}
