package ch01;

public class Ex05 {
	public static void main(String[] args) {
		// String 
		// 문자를 사용할 때 씀
		// 기본 데이터 타입이 아님
		String a;
		a = "hello";
		System.out.println(a); // String은 ""(큰따옴표) 안에 사용해야함
		
		String b = " ";		//" "(공백)도 문자로 인식
		System.out.println(b);
		
		String c = "hello world";
		System.out.println(c);
	}
}
